 public ArrayList<transaction_history_class> userList(){
ArrayList<transaction_history_class> userList= new ArrayList<>();//create object named arraylist
try{
     Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");  // load the driver
           String url = "jdbc:sqlserver://localhost:1433;databaseName=bankoneer_db;user=sa;password=bishal";
           Connection con = DriverManager.getConnection(url);
           String query1 = "Select * from transaction_history WHERE status=?";
           PreparedStatement pst= con.prepareStatement(query1);
           //Statement st = con.createStatement();
          dep = "A & W : LOADED To Wallet Balance";// "A & W : LOADED To Account Balance";
           pst.setString(1, dep);
      ResultSet rs = pst.executeQuery();
      //create object for user
      transaction_history_class user;
      while(rs.next()){ 
user= new transaction_history_class(rs.getInt("sno"),rs.getString("date_time"),rs.getString("account_no"),rs.getString("status"),rs.getString("type"),rs.getString("amount"),rs.getString("total_balance"),rs.getString("wallet_balance"));
 //add user to the user list
      userList.add(user);
      }
      }
catch(Exception e){
    JOptionPane.showMessageDialog(null, e);
            }
      return userList;
}
 public void showall_user(){
    ArrayList<transaction_history_class> list = userList();
    DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
    Object[] row = new Object[8];
    for(int i=0; i<list.size();i++){
       row[0]=list.get(i).getsno();
       row[1]=list.get(i).getdate_time();
       row[2]=list.get(i).getaccount_no();       
        row[3]=list.get(i).gettype();
        row[4]=list.get(i).getstatus();
        row[5]=list.get(i).getamount();
        row[6]=list.get(i).gettotal_balance();
        row[7]=list.get(i).getwallet_balance();
        
        model.addRow(row);
    }
    }
private void filter(String query){
     dm=(DefaultTableModel) jTable1.getModel();
     TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(dm);
     jTable1.setRowSorter(tr);
     tr.setRowFilter(RowFilter.regexFilter(query));
 }
